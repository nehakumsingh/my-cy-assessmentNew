// Custom Command for Login functionality
 Cypress.Commands.add('login', (username, password) => { 
    cy.visit('/')
    cy.get('#user-name').type(username ? username: Cypress.env('username'))
    cy.get('#password').type(password ? password: Cypress.env('password'))
    cy.get('#login-button').click()
})

